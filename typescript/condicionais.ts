/* condicionais  - if */
let idade : number = 21
if (idade >= 18) {
    console.log("Você é maior de idade.")
    console.log("Já pode ser preso.")
}
console.log("Terminou!")

/* condicionais - if, else */
let hora : number = 9;
if (hora < 12) {
    console.log("Bom dia!");
} 
else if (hora < 18) {
    console.log("Boa tarde!");
} else {
    console.log("Boa noite!");
}

